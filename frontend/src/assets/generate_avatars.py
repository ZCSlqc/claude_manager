USER_GRID = 12
SESSION_GRID = 8


def generate_user_avatars():
    out = os.path.join(OUTPUT_DIR, "user")
    os.makedirs(out, exist_ok=True)
    for i in range(100):
        pair = USER_PALETTES[i]
        tmpl = USER_TEMPLATES[i]
        cell = 5
        size = USER_GRID * cell
        pixels = bytearray(size * size * 4)
        for y in range(USER_GRID):
            for x in range(USER_GRID):
                v = tmpl[y][x]
                if not v:
                    continue
                colors = {
                    1: pair[0], 2: pair[1], 3: pair[2], 4: pair[3],
                    5: pair[4], 6: pair[5], 7: pair[6], 8: pair[7],
                    9: rgb('#ffffff'),
                }
                r, g, b = colors.get(v, (0, 0, 0))
                ox, oy = x * cell, y * cell
                for dy in range(cell):
                    for dx in range(cell):
                        idx = ((oy + dy) * size + (ox + dx)) * 4
                        pixels[idx] = r
                        pixels[idx + 1] = g
                        pixels[idx + 2] = b
                        pixels[idx + 3] = 255
        path = os.path.join(out, f"avatar_{i+1:03d}.png")
        _write_png(path, pixels, size, size)
        print(f"  user avatar_{i+1:03d}.png")


def generate_session_avatars():
    out = os.path.join(OUTPUT_DIR, "session")
    os.makedirs(out, exist_ok=True)
    for i in range(100):
        tmpl = SESSION_TEMPLATES[i % len(SESSION_TEMPLATES)]
        fg, eye, body = SESSION_PALETTES[i % len(SESSION_PALETTES)]
        if i >= 10:
            use_alt = (i // 10) % 2
            if use_alt:
                fg, eye, body = body, eye, fg
        cell = 8
        size = SESSION_GRID * cell
        pixels = bytearray(size * size * 4)
        for y in range(SESSION_GRID):
            for x in range(SESSION_GRID):
                v = tmpl[y][x]
                if not v:
                    continue
                r, g, b = (rgb(fg) if v == 1 else rgb(eye) if v == 2 else rgb(body))
                ox, oy = x * cell, y * cell
                for dy in range(cell):
                    for dx in range(cell):
                        idx = ((oy + dy) * size + (ox + dx)) * 4
                        pixels[idx] = r
                        pixels[idx + 1] = g
                        pixels[idx + 2] = b
                        pixels[idx + 3] = 255
        path = os.path.join(out, f"avatar_{i+1:03d}.png")
        _write_png(path, pixels, size, size)
        print(f"  session avatar_{i+1:03d}.png")


