USER_GRID = 8
SESSION_GRID = 8

def generate_char_avatar(filepath, char, colors):
    """生成一个字符头像"""
    tmpl = CHAR_TEMPLATES[char]
    fg, bg, highlight = colors
    size = USER_GRID
    pixels = bytearray(size * size * 4)
    for y in range(USER_GRID):
        for x in range(USER_GRID):
            v = tmpl[y][x]
            if not v:
                continue
            r, g, b = rgb(bg) if v == 0 else (rgb(fg) if v == 1 else rgb(highlight))
            ox, oy = x, y
            idx = (oy * size + ox) * 4
            pixels[idx] = r
            pixels[idx + 1] = g
            pixels[idx + 2] = b
            pixels[idx + 3] = 255
    write_png(filepath, pixels, size, size)

def generate_user_avatars():
    out = os.path.join(OUTPUT_DIR, "user")
    os.makedirs(out, exist_ok=True)
    for i in range(len(CHAR_LIST)):
        char = CHAR_LIST[i]
        colors = COLOR_PALETTES[i % len(COLOR_PALETTES)]
        path = os.path.join(out, f"avatar_{i+1:03d}.png")
        generate_char_avatar(path, char, colors)
        print(f"  user avatar_{i+1:03d}.png ({char})")

