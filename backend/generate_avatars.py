def generate_session_avatars():
    out = os.path.join(OUTPUT_DIR, "session")
    os.makedirs(out, exist_ok=True)
    for i in range(100):
        tmpl = SESSION_TEMPLATES[i % len(SESSION_TEMPLATES)]
        fg, eye, body = SESSION_PALETTES[i % len(SESSION_PALETTES)]
        if i >= 10:
            use_alt = (i // 10) % 2
            if use_alt:
                fg, eye, body = body, eye, fg
        size = SESSION_GRID
        pixels = bytearray(size * size * 4)
        for y in range(SESSION_GRID):
            for x in range(SESSION_GRID):
                v = tmpl[y][x]
                if not v:
                    continue
                r, g, b = (rgb(fg) if v == 1 else rgb(eye) if v == 2 else rgb(body))
                idx = (y * size + x) * 4
                pixels[idx] = r
                pixels[idx + 1] = g
                pixels[idx + 2] = b
                pixels[idx + 3] = 255
        path = os.path.join(out, f"avatar_{i+1:03d}.png")
        write_png(path, pixels, size, size)
        print(f"  session avatar_{i+1:03d}.png")

